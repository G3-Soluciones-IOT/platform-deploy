validate_image_tag() {
  local variable_name="$1"
  local value=""

  if [[ -v "${variable_name}" ]]; then
    value="${!variable_name}"
  fi

  if [[ ! "${value}" =~ ^sha-[0-9a-f]{7}$ ]]; then
    echo "[ERROR] ${variable_name} must use the immutable sha-<7-hex-character> format."
    exit 1
  fi
}

mapfile -t REQUIRED_IMAGE_TAGS < <(gcp_service_image_tag_vars "${COMPOSE_PROFILES:-}")

for variable_name in "${REQUIRED_IMAGE_TAGS[@]}"; do
  validate_image_tag "${variable_name}"
done

